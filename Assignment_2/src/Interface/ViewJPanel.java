    public void displayJTable()
    {   
        DefaultTableModel dtm = (DefaultTableModel)DisplayTable.getModel();
        dtm.setRowCount(0);
        
        for(Resume resume : rehis.getResumeHistory())
        {
            Object row[] = new Object[5];
            row[0] = resume;
            row[1] = resume.getLastname();
            row[2] = resume.getGender();
            row[3] = resume.getNation();
            row[4] = resume.getBirthday_year();
            dtm.addRow(row);
        }
        
    }    
