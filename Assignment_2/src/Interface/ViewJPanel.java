    private void DetailBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DetailBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = DisplayTable.getSelectedRow();
        
        if(selectedrow >= 0)
        {
            Resume resume = (Resume)DisplayTable.getValueAt(selectedrow, 0);
            
            FnameTextF.setText(resume.getFirstname());
            LnameTextF.setText(resume.getLastname());
            GenderTextF.setText(resume.getGender());
            NationTextF.setText(resume.getNation());
            MonthTextF.setText(resume.getBirthday_month());
            DateTextF.setText(resume.getBirthday_date());
            YearTextF.setText(resume.getBirthday_year());
            AffilTextF.setText(resume.getAffiliation());
            
            String path = resume.getImage();
            ImageIcon ThisImg = new ImageIcon(path);
//            ImageJLable.setIcon(ThisImg);            
            Image img1 = ThisImg.getImage().getScaledInstance(107, 107, WIDTH);
            ImageIcon img2 = new ImageIcon(img1);
            ImageJLable.setIcon(img2);


            AddressTextF.setText(resume.getAddress());
            AptTextF.setText(resume.getApt());
            CityTextF.setText(resume.getCity());
            StateTextF.setText(resume.getState());
            ZipTextF.setText(String.valueOf(resume.getZipcode()));
            MobileTextF.setText(String.valueOf(resume.getMobile()));
            EmailTextF.setText(resume.getEmail());
            
            Degree1TextF.setText(resume.getDegree1());
            Major1TextF.setText(resume.getMajor1());
            Status1TextF.setText(resume.getStatus1());
            Core1TextF.setText(resume.getCore1());
            Degree2TextF.setText(resume.getDegree2());
            Major2TextF.setText(resume.getMajor2());
            Status2TextF.setText(resume.getStatus2());
            Core2TextF.setText(resume.getCore2());
            
            JavaCheck.setSelected(resume.isJava());
            CplusCheck.setSelected(resume.isCplus());
            PhpCheck.setSelected(resume.isPhp());
            SqlCheck.setSelected(resume.isSql());
            LevelTextF.setText(resume.getLevel());
            InstructionTextF.setText(resume.getInstruction());
            StatementTextArea.setText(resume.getStatement());
            
        }else{
            JOptionPane.showMessageDialog(null, "Please Select a Item");
        }
        
    }//GEN-LAST:event_DetailBtnActionPerformed

    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = DisplayTable.getSelectedRow();
        
        if(selectedrow >= 0)
        {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you Like to delete it?" , "Warning" , dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION)
            {
                Resume resume = (Resume)DisplayTable.getValueAt(selectedrow, 0);
                rehis.deleteResume(resume);
                JOptionPane.showMessageDialog(null, "Vital Sign Deleted Successfully");
                displayJTable();
                
                FnameTextF.setText("");
                LnameTextF.setText("");
                GenderTextF.setText("");
                NationTextF.setText("");
                MonthTextF.setText("");
                DateTextF.setText("");
                YearTextF.setText("");
                AffilTextF.setText("");
                ImageJLable.setIcon(null);
            
                AddressTextF.setText("");
                AptTextF.setText("");
                CityTextF.setText("");
                StateTextF.setText("");
                ZipTextF.setText("");
                MobileTextF.setText("");
                EmailTextF.setText("");
            
                Degree1TextF.setText("");
                Major1TextF.setText("");
                Status1TextF.setText("");
                Core1TextF.setText("");
                Degree2TextF.setText("");
                Major2TextF.setText("");
                Status2TextF.setText("");
                Core2TextF.setText("");
            
                JavaCheck.setSelected(false);
                CplusCheck.setSelected(false);
                PhpCheck.setSelected(false);
                SqlCheck.setSelected(false);
                LevelTextF.setText("");
                InstructionTextF.setText("");
        }   
    }else{
            JOptionPane.showMessageDialog(null, "Please Select a Item");
         } 
        
    }//GEN-LAST:event_DeleteBtnActionPerformed


