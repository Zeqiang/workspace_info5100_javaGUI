    public void populateDrug(){
        
        DefaultTableModel model = (DefaultTableModel) DrugTable.getModel();
        model.setRowCount(0);
        
        for(Drug drug : medicine.getDrugCatalog().getDrugsList()){
            Object[] row = new Object[2];
            row[0] = drug;
            row[1] = drug.getType();
            row[2] = drug.getDurgUsage();
            model.addRow(row);
        }
    }
    
    
    public void populateIngredients(Drug drug){
        DefaultTableModel model = (DefaultTableModel) ComponentTable.getModel();
        model.setRowCount(0);
        
        for(Ingredients ingredients : drug.getIngredientslist().getIngredientsList()){
            Object[] row = new Object[2];
            row[0] = ingredients;
            row[1] = ingredients.getFunction();
            row[2] = ingredients.getActiveDose();
            model.addRow(row);
        }
    }

