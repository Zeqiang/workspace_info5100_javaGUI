    private void receiveResearchjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_receiveResearchjButtonActionPerformed
        // TODO add your handling code here:
       int selectedRow = testRequestjTable.getSelectedRow();

        if (selectedRow >= 0) {
            ResearchRequest request = (ResearchRequest) testRequestjTable.getValueAt(selectedRow, 0);
            if(request.getStatus() == WorkRequest.STATUS_3)  { 
                request.setVolunteermanagerEmployee(useraccount.getEmployee());
                request.setStatus(WorkRequest.STATUS_4);
                populateRequestjTable();
            }else{
                JOptionPane.showMessageDialog(null, "Plesae Select Proper Medicine Test");
            }
        }
    }//GEN-LAST:event_receiveResearchjButtonActionPerformed

    private void allocateVolunteerjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_allocateVolunteerjButtonActionPerformed
        // TODO add your handling code here:
        int requestselectedRow = testRequestjTable.getSelectedRow();
        int volunteerselectedRow = volunteersCatalogjTable.getSelectedRow();

        if (requestselectedRow >= 0 && volunteerselectedRow >= 0) {
            Volunteer v  =  (Volunteer) volunteersCatalogjTable.getValueAt(volunteerselectedRow, 0);
            if(v.getStatus().equals("Available")){
                ResearchRequest request = (ResearchRequest) testRequestjTable.getValueAt(requestselectedRow, 0);
                if(request.getStatus() == WorkRequest.STATUS_4){
                
                    request.getVolunteersList().addVolunteer(v);
                    v.setStatus("On Test");
                    JOptionPane.showMessageDialog(null, "Volunteer Inserted Successfully");
                    populateRequestjTable();
                    populatevolunteersCatalogjTable();
                
                    if(request.getVolunteersAmount() == request.getVolunteersList().getVolunteerList().size()){
                        request.setStatus(WorkRequest.STATUS_5);
                    
                        populateRequestjTable();
                        return;
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Please Select the Right Test");
                }
            }else if(v.getStatus().equals("On Test")){
                JOptionPane.showMessageDialog(null, "Volunteer is On Test");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Plesae Select The Rows");
        }
    }//GEN-LAST:event_allocateVolunteerjButtonActionPerformed

