    public void populatevolunteersCatalogjTable(){
        DefaultTableModel model = (DefaultTableModel) volunteersCatalogjTable.getModel();
        model.setRowCount(0);
        
        for (Volunteer v : enterprise.getVolunteerCatalog().getVolunteerList()) {
            Object[] row = new Object[2];
            row[0] = v;
            row[1] = v.getStatus();
            model.addRow(row);
        }
    }
    
    
    
    public void populateRequestjTable(){
        DefaultTableModel model = (DefaultTableModel) testRequestjTable.getModel();
        model.setRowCount(0);
        
        for (WorkRequest workrequest : enterprise.getWorkQueue().getWorkRequestList()) {
            ResearchRequest s = (ResearchRequest) workrequest;
            
                Object[] row = new Object[5];
                row[0] = s;
                row[1] = s.getVolunteermanagerEmployee();
                row[2] = s.getStatus();
                row[3] = s.getVolunteersAmount();
                row[4] = s.getVolunteersList().getVolunteerList().size();
                model.addRow(row);
                
        }
    }
