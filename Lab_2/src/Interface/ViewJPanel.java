    public void disinJTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)ViewJtb.getModel();
        dtm.setRowCount(0);
        
        for(VitalSign vs : vsh.getVitalSignHistory())
        {
            Object row[] = new Object[2];
            row[0] = vs;
            row[1] = vs.getBloodPressure();
            dtm.addRow(row);
        }
        
    }
    
