    private void DetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DetailsBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = ViewJtb.getSelectedRow();
        
        if(selectedrow >= 0)
        {
            VitalSign vs = (VitalSign)ViewJtb.getValueAt(selectedrow, 0);
            TempTextF.setText(String.valueOf(vs.getTemperature()));
            BloodTextF.setText(String.valueOf(vs.getBloodPressure()));
            PulseTextF.setText(String.valueOf(vs.getPulse()));
            DateTextF.setText(vs.getDate());
        }else{
            JOptionPane.showMessageDialog(null, "Please Select a Item");
        }
        
    }//GEN-LAST:event_DetailsBtnActionPerformed

    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedrow = ViewJtb.getSelectedRow();
        
        if(selectedrow >= 0)
        {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you Like to delete it?" , "Warning" , dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION)
            {
                VitalSign vs = (VitalSign)ViewJtb.getValueAt(selectedrow, 0);
                vsh.deleteVitalSign(vs);
                JOptionPane.showMessageDialog(null, "Vital Sign Deleted Successfully");
                disinJTable();
                TempTextF.setText("");
                BloodTextF.setText("");
                PulseTextF.setText("");
                DateTextF.setText("");
        }   
    }else{
            JOptionPane.showMessageDialog(null, "Please Select a Item");
         } 
    }//GEN-LAST:event_DeleteBtnActionPerformed

