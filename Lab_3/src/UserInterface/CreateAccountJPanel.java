    private void CreateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBtnActionPerformed
        // TODO add your handling code here:
        try{
            String routingNumber = RoutingNumTextF.getText();
            String accountNumber = AccountNumTextF.getText();
            String bankName = BankNameTextF.getText();
            int balance = Integer.parseInt(BalanceTextF.getText());
        
            Account account = accountDirectory.addAccount();
        
            account.setRoutingNumber(routingNumber);
            account.setAccountNumber(accountNumber);
            account.setBankName(bankName);
            account.setBalance(balance);
        
            JOptionPane.showMessageDialog(null, "Account Created Successfully!", "Information", JOptionPane.INFORMATION_MESSAGE);
            
            RoutingNumTextF.setText("");
            AccountNumTextF.setText("");
            BankNameTextF.setText("");
            BalanceTextF.setText("");
        }catch(NumberFormatException nfex){
            JOptionPane.showMessageDialog(null, "Correct format reuired!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_CreateBtnActionPerformed

