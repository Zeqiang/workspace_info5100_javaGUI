    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        String userName = txtUserName.getText().trim();
        String password = txtPassword.getText().trim();
        
        if(userName.equals("") || password.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter all the Fields", "Waring", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        UserAccount userAccount = business.getUserAccountDirectory().addUserAccount();
        userAccount.setUserName(userName);
        userAccount.setPassword(password);
        userAccount.setPerson((Person)PersonCombox.getSelectedItem());
        userAccount.setRole(String.valueOf(RoleCombox.getSelectedItem()));
        
        if(YesRBtn.isSelected()){
            userAccount.setIsActive(true);
        }else{
            userAccount.setIsActive(false);
        }
        
        JOptionPane.showMessageDialog(null, "Successfully Saved", "Information", JOptionPane.INFORMATION_MESSAGE);
        
        txtUserName.setText("");
        txtPassword.setText("");
    }//GEN-LAST:event_AddBtnActionPerformed


