    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
        // TODO add your handling code here:
        String userName = txtUserName.getText().trim();
        String password = String.valueOf(txtPassword.getPassword());
        boolean flag = false;
        
        for(UserAccount ua : business.getUserAccountDirectory().getUserAccountList()){
            if(ua.getUserName().equals(userName) && ua.getPassword().equals(password)){
                if(ua.getRole().equals(UserAccount.ADMIN_ROLE)){
                    processContainer.removeAll();
                    AdminWorkAreaJP awjp = new AdminWorkAreaJP(processContainer, business);
                    processContainer.add("AdminWorkAreaJP" , awjp);
                    CardLayout layout = (CardLayout) processContainer.getLayout();
                    layout.next(processContainer);
                    flag = true;
                    break;
                }else if(ua.getRole().equals(UserAccount.EMPLOYEE_ROLE)){
                    EmployeeWorkAreaJP ewjp = new EmployeeWorkAreaJP(processContainer , ua);
                    processContainer.add("EmployeeWorkAreaJP" , ewjp);
                    CardLayout layout = (CardLayout) processContainer.getLayout();
                    layout.next(processContainer);
                    flag = true;
                    break;
                }
            }
        }
        if(flag == false){
            JOptionPane.showMessageDialog(null, "Invalid  userName/password", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        txtUserName.setEnabled(false);
        txtPassword.setEnabled(false);
        LoginBtn.setEnabled(false);
        LogoutBtn.setEnabled(true);
    }//GEN-LAST:event_LoginBtnActionPerformed

    private void LogoutBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogoutBtnActionPerformed
        // TODO add your handling code here:
        txtUserName.setText("");
        txtPassword.setText("");
        
        txtUserName.setEnabled(true);
        txtPassword.setEnabled(true);
        LoginBtn.setEnabled(true);
        LogoutBtn.setEnabled(false);
        
        processContainer.removeAll();
        
        JPanel blankJP = new JPanel();
        processContainer.add("BlankJPanel" , blankJP);
        CardLayout layout = (CardLayout) processContainer.getLayout();
        layout.next(processContainer);
    }//GEN-LAST:event_LogoutBtnActionPerformed

