    private void RemoveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = UserAccountJtb.getSelectedRow();
        if(selectedRow >= 0){
            int dialogBtn = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do You Want to Remove the User Accout?", "Warning", dialogBtn);
            if(dialogResult == JOptionPane.YES_OPTION){
                UserAccount userAccount = (UserAccount) UserAccountJtb.getValueAt(selectedRow, 0);
                if(userAccount.getRole().equals(UserAccount.ADMIN_ROLE)){
                    JOptionPane.showMessageDialog(null, "Admin cannot be removed", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                business.getUserAccountDirectory().removeUserAccount(userAccount);
                JOptionPane.showMessageDialog(null, "User Account Removed Successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
                refreshTable();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please Select a Row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_RemoveBtnActionPerformed

